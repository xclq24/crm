package com.tarena.crm.entity;

public class Source {
	
	private int id;
	private String source;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	
	public Object[] toArray() {
		return new Object[] { source };
	}
}
